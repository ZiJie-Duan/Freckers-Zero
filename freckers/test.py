import numpy as np
from scipy.signal import convolve2d

def dilate_matrix_convolution(arr):
    # 定义3x3的卷积核（八邻域）
    kernel = np.ones((3, 3), dtype=np.uint8)
    # 执行卷积，若卷积结果 >0，说明周围有1
    convolved = convolve2d(arr, kernel, mode='same', boundary='fill', fillvalue=0)
    print(convolved)
    # 合并原始矩阵和膨胀后的区域
    result = np.where(convolved > 1, 1, arr)
    return result

